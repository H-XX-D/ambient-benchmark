#!/usr/bin/env node
// AMBIENT ADVERSARIAL - supersession-distance
// =====================================================================================
// ATTACK: does the store return the CURRENT value of a fact when the update is buried
// far from the query, WITHOUT a hand-declared supersede edge (relying only on retrieval
// ranking, the realistic case an app hits when it just writes the new value as a new
// cell)? We plant value A, then a run of near-duplicate cells that restate A (the fact
// gets re-mentioned as it ages), then the update B as the newest write, at growing
// distance d = 1,2,4,8,16,32,64,128,256. For each d we query for the fact and read the
// top-ranked value, over three arms:
//
//   undeclared           : B written with NO edge. Pure retrieval ranking must surface it.
//   declared-supersede   : B carries supersedes edges to A and every restatement.
//   declared-contradicts : B carries contradicts edges to A and every restatement.
//
// SURFACE SCORED (in class): the fused context retrieval used by compileContext() in
// compile.js. It is reproduced here VERBATIM (store.search(limit:30) -> degreeMap ->
// fuseCandidates(limit:10) with TASK_CONTEXT_KIND_FACTOR). This path is the one that
// claims a recency prior, so "surface the newer value" is a capability it advertises.
// We also report the raw store.search() top-1 and the active() listing as diagnostics.
//
// FAIRNESS
//  1. Both-direction headroom. A positive metric-control store (only B) MUST score pass;
//     a negative metric-control store (only A and restatements, no B) MUST score fail.
//     Those two prove the harness can read 100 and 0 before any arm is scored. The
//     declared-supersede arm is the should-pass side; the undeclared arm is where a
//     stale answer can occur. Stale = fail, current = pass, both reachable.
//  2. Input not authored to the mechanism. The corpus is generated by one rule: the
//     stale value is restated d times (once per day) as the fact ages, then the update
//     is a single newest write. A, its restatements, and B share ONE phrasing template
//     of equal token length, differing only in the value token and a dedup id token, so
//     bm25 is symmetric and nothing is hand-tuned to rank B up or down. 10 independent
//     facts (different attributes and values) run per d so accuracy is a percentage.
//  3. Same-capability-class. We do NOT ask the store to auto-detect from raw text that
//     B supersedes A with no edge. That capability (auto-supersede-from-text) does not
//     exist in this build and is reported UNTESTED, not scored. The undeclared arm only
//     asks whether the recency-aware ranking floats the newest write to the top, which
//     is an advertised behavior of the fused path.
//  4. Report WHERE it breaks (the d where undeclared current-value accuracy falls below
//     90 percent), with the structural reason, not just that it passed or failed.
//
// Deterministic: fixed fact list, fixed ISO timestamps (1 day apart from 2025-01-01),
// no RNG, no Date.now, no Math.random.

import { pathToFileURL } from "node:url";

// Same module _recall.mjs re-exports (../dist -> ../vendor/recall/dist). Imported by
// absolute file URL so the harness runs from any location despite spaces in the path.
const DIST = "/Users/hendrixx./SENTINEL memory system  Benchmark /vendor/recall/dist/index.js";
const R = await import(pathToFileURL(DIST).href);
const { SqliteStore, admit, fuseCandidates, degreeMap, TASK_CONTEXT_KIND_FACTOR } = R;

const DISTANCES = [1, 2, 4, 8, 16, 32, 64, 128, 256];
const POOL = 30; // compileContext wideHits limit in compile.js. The reranker only sees this many.
const CLIFF_THRESHOLD = 0.9; // undeclared accuracy below this = "broken" for the cliff report.

const BASE = Date.parse("2025-01-01T00:00:00.000Z");
const DAY = 86400000;
const isoAt = (n) => new Date(BASE + n * DAY).toISOString();

// 10 independent facts. Every value token is a SINGLE FTS token on both sides so A and B
// documents have identical length; the query is the 3-word attribute (no stopwords), and
// value tokens never appear in the query, so bm25 depends only on the shared attribute.
const FACTS = [
  { attr: "primary datacenter region", a: "oregon", b: "frankfurt" },
  { attr: "production database engine", a: "postgres", b: "cockroachdb" },
  { attr: "default deployment target", a: "staging", b: "canary" },
  { attr: "release owner name", a: "alice", b: "dwight" },
  { attr: "api rate tier", a: "bronze", b: "platinum" },
  { attr: "preferred message broker", a: "rabbitmq", b: "kafka" },
  { attr: "canonical timezone name", a: "utc", b: "pacific" },
  { attr: "feature flag rollout", a: "tenth", b: "full" },
  { attr: "billing currency code", a: "usd", b: "eur" },
  { attr: "frontend build tool", a: "webpack", b: "vite" },
];

// One phrasing template. `id` is the only per-cell varying title token (keeps contentKey
// unique so admission does not dedup) and is not a query term. Body carries the value.
function cellProps(fact, id, value, edges) {
  return {
    kind: "obs",
    title: `${fact.attr} value record ${id}`,
    body: `the ${fact.attr} is ${value}`,
    confidence: 0.8,
    project: "adv-supersession",
    tenant: "local",
    topics: ["fact"],
    entities: [fact.attr.replace(/\s+/g, "_")],
    ...(edges && edges.length ? { edges } : {}),
  };
}

function W(store, props, nowIso) {
  const res = admit(props, { store, now: nowIso });
  if (!res || res.accepted !== true || !res.cell) {
    throw new Error(`not admitted: ${props.title} -> ${JSON.stringify(res?.issues ?? res)}`);
  }
  return res.cell;
}

// Classify a cell's value against a fact: which value does it assert?
function classify(cell, fact) {
  const hay = `${cell.title} ${cell.body}`.toLowerCase();
  if (hay.includes(fact.b)) return "B";
  if (hay.includes(fact.a)) return "A";
  return "other";
}

// VERBATIM reproduction of compileContext() fusion (compile.js lines 17 to 25).
function fusedHits(store, query, nowDate, limit = 10) {
  const wide = store.search(query, { limit: POOL });
  const keys = wide.map((h) => h.cell.key);
  const dm = degreeMap(store, keys);
  const cands = wide.map((h) => ({ cell: h.cell, bm25: h.score, degree: dm.get(h.cell.key) ?? 0 }));
  const fused = fuseCandidates(cands, limit, nowDate, { kindLexicalFactor: TASK_CONTEXT_KIND_FACTOR });
  return { fused, wide };
}

// Build one fact store for a given arm and distance, then read every surface.
// arm: "undeclared" | "supersede" | "contradicts".
function runTrial(fact, d, arm) {
  const store = new SqliteStore(":memory:");
  try {
    // A at day 0.
    const aCell = W(store, cellProps(fact, "a0", fact.a), isoAt(0));
    // d restatements of A at days 1..d (the buried filler that re-mentions the stale value).
    const restated = [];
    for (let k = 1; k <= d; k++) {
      restated.push(W(store, cellProps(fact, `a${k}`, fact.a), isoAt(k)));
    }
    // B (the update) as the newest write at day d+1.
    let edges;
    if (arm === "supersede") {
      edges = [aCell, ...restated].map((c) => ({ relation: "supersedes", target: c.key }));
    } else if (arm === "contradicts") {
      edges = [aCell, ...restated].map((c) => ({ relation: "contradicts", target: c.key }));
    }
    const bCell = W(store, cellProps(fact, "b0", fact.b, edges), isoAt(d + 1));

    const query = fact.attr;
    const nowDate = new Date(BASE + (d + 2) * DAY);

    // Scored surface: fused context retrieval (has the recency prior).
    const { fused, wide } = fusedHits(store, query, nowDate, 10);
    const fusedTop = fused.length ? classify(fused[0].cell, fact) : "none";

    // Diagnostic 1: raw lexical search top-1 (no currency model). Reported, not scored.
    const rawList = store.search(query, { limit: 10 });
    const rawTop = rawList.length ? classify(rawList[0].cell, fact) : "none";

    // Diagnostic 2: is the newest write even inside the reranker's candidate pool?
    const bInPool = wide.some((h) => h.cell.key === bCell.key);
    // B's rank in the full lexical ordering (limit large enough to return everything).
    const fullList = store.search(query, { limit: 100000 });
    const bRawRank = fullList.findIndex((h) => h.cell.key === bCell.key); // 0-based, -1 if absent
    const matching = fullList.length;

    // Diagnostic 3: active listing. Is B the only active value cell?
    const activeVals = new Set(store.active().map((c) => classify(c, fact)).filter((v) => v === "A" || v === "B"));
    const activeIsBOnly = activeVals.size === 1 && activeVals.has("B");

    return { fusedTop, rawTop, bInPool, bRawRank, matching, activeIsBOnly };
  } finally {
    store.close();
  }
}

// Metric controls prove the harness can read both 0 and 100 before any arm is judged.
function metricControls() {
  const fact = FACTS[0];
  const query = fact.attr;
  const nowDate = new Date(BASE + 10 * DAY);

  // Positive: only B exists. Fused top MUST be B (pass).
  const posStore = new SqliteStore(":memory:");
  W(posStore, cellProps(fact, "b0", fact.b), isoAt(1));
  const pos = fusedHits(posStore, query, nowDate).fused;
  const posTop = pos.length ? classify(pos[0].cell, fact) : "none";
  posStore.close();

  // Negative: only A and restatements exist, no B. Fused top MUST be A (fail).
  const negStore = new SqliteStore(":memory:");
  W(negStore, cellProps(fact, "a0", fact.a), isoAt(0));
  for (let k = 1; k <= 5; k++) W(negStore, cellProps(fact, `a${k}`, fact.a), isoAt(k));
  const neg = fusedHits(negStore, query, nowDate).fused;
  const negTop = neg.length ? classify(neg[0].cell, fact) : "none";
  negStore.close();

  return {
    positivePass: posTop === "B",
    negativeFail: negTop === "A",
    posTop,
    negTop,
  };
}

// ------------------------------------------------------------------------------------
function main() {
  const t0 = Date.now === undefined ? 0 : Date.now();
  const backend = new SqliteStore(":memory:").stats().lexicalBackend;

  console.log("==================================================================================");
  console.log("AMBIENT ADVERSARIAL - supersession-distance");
  console.log("==================================================================================");
  console.log(`lexical backend: ${backend}   facts/d: ${FACTS.length}   distances: ${DISTANCES.join(", ")}`);
  console.log(`scored surface : fused context retrieval (compileContext fusion, candidate pool = ${POOL})`);
  console.log("");

  // Controls.
  const ctl = metricControls();
  console.log("---- metric controls (harness teeth: must read both 100 and 0) ----");
  console.log(`positive control (store has only B)              : top=${ctl.posTop}  -> ${ctl.positivePass ? "PASS (reads 100)" : "BROKEN"}`);
  console.log(`negative control (store has only A/restatements) : top=${ctl.negTop}  -> ${ctl.negativeFail ? "correctly FAIL (reads 0)" : "BROKEN"}`);
  if (!ctl.positivePass || !ctl.negativeFail) {
    console.log("\nHARNESS BROKEN: a control did not behave. Aborting; results below would be meaningless.");
    return;
  }
  console.log("");

  const arms = ["undeclared", "supersede", "contradicts"];
  const results = {}; // arm -> d -> aggregate
  for (const arm of arms) {
    results[arm] = {};
    for (const d of DISTANCES) {
      let fusedB = 0, rawB = 0, inPool = 0, activeBOnly = 0;
      let bRankSum = 0, matchingSum = 0, bAbsent = 0;
      for (const fact of FACTS) {
        const r = runTrial(fact, d, arm);
        if (r.fusedTop === "B") fusedB++;
        if (r.rawTop === "B") rawB++;
        if (r.bInPool) inPool++;
        if (r.activeIsBOnly) activeBOnly++;
        if (r.bRawRank < 0) bAbsent++; else bRankSum += r.bRawRank;
        matchingSum += r.matching;
      }
      const n = FACTS.length;
      results[arm][d] = {
        fusedAcc: fusedB / n,
        rawAcc: rawB / n,
        inPoolFrac: inPool / n,
        activeBOnlyFrac: activeBOnly / n,
        avgBRank: bAbsent === n ? null : bRankSum / (n - bAbsent),
        avgMatching: matchingSum / n,
      };
    }
  }

  // Scored table: fused current-value accuracy per arm per d.
  console.log("---- SCORED: fused current-value accuracy vs distance d (percent B on top) ----");
  const header = ["d".padStart(5), "matching".padStart(9), "undeclared".padStart(11), "supersede".padStart(10), "contradicts".padStart(12)];
  console.log(header.join(" "));
  for (const d of DISTANCES) {
    const row = [
      String(d).padStart(5),
      String(Math.round(results.undeclared[d].avgMatching)).padStart(9),
      pct(results.undeclared[d].fusedAcc).padStart(11),
      pct(results.supersede[d].fusedAcc).padStart(10),
      pct(results.contradicts[d].fusedAcc).padStart(12),
    ];
    console.log(row.join(" "));
  }
  console.log("");

  // Diagnostics for the undeclared arm: WHY it breaks (pool membership + B's lexical rank).
  console.log("---- undeclared arm diagnostics (why it breaks): pool = 30 ----");
  console.log(["d".padStart(5), "B_in_pool%".padStart(11), "avg_B_rawrank".padStart(14), "active_B_only%".padStart(15)].join(" "));
  for (const d of DISTANCES) {
    const u = results.undeclared[d];
    console.log([
      String(d).padStart(5),
      pct(u.inPoolFrac).padStart(11),
      (u.avgBRank === null ? "n/a" : u.avgBRank.toFixed(1)).padStart(14),
      pct(u.activeBOnlyFrac).padStart(15),
    ].join(" "));
  }
  console.log("");

  // Raw lexical top-1 (diagnostic only, not scored: raw search claims no currency).
  console.log("---- diagnostic: raw store.search() top-1 accuracy (NOT scored, no currency claim) ----");
  console.log(["d".padStart(5), "undeclared".padStart(11), "supersede".padStart(10), "contradicts".padStart(12)].join(" "));
  for (const d of DISTANCES) {
    console.log([
      String(d).padStart(5),
      pct(results.undeclared[d].rawAcc).padStart(11),
      pct(results.supersede[d].rawAcc).padStart(10),
      pct(results.contradicts[d].rawAcc).padStart(12),
    ].join(" "));
  }
  console.log("");

  // Cliff report for the undeclared (scored) arm.
  let cliff = null;
  for (const d of DISTANCES) {
    if (results.undeclared[d].fusedAcc < CLIFF_THRESHOLD) { cliff = d; break; }
  }
  console.log("---- WHERE IT BREAKS ----");
  if (cliff === null) {
    console.log(`undeclared arm never fell below ${Math.round(CLIFF_THRESHOLD * 100)}% within tested range (d up to ${DISTANCES[DISTANCES.length - 1]}).`);
  } else {
    const prevIdx = DISTANCES.indexOf(cliff) - 1;
    const prev = prevIdx >= 0 ? DISTANCES[prevIdx] : null;
    console.log(`undeclared arm first drops below ${Math.round(CLIFF_THRESHOLD * 100)}% at d=${cliff} (accuracy ${pct(results.undeclared[cliff].fusedAcc)}).`);
    if (prev !== null) console.log(`  it still held at d=${prev} (accuracy ${pct(results.undeclared[prev].fusedAcc)}).`);
    console.log(`  structural cause: the fused reranker only reorders the top ${POOL} lexical candidates.`);
    console.log(`  once the stale value has been restated enough that matching cells exceed ${POOL},`);
    console.log(`  the single newest write (B) falls outside the candidate pool and the recency prior`);
    console.log(`  can never lift it. B_in_pool% collapses at the same d. supersede escapes this because`);
    console.log(`  it flips A/restatements to status=superseded, so store.search (status='active') stops`);
    console.log(`  returning them and B is the only value cell left in the pool.`);
  }
  console.log("");

  console.log("---- UNTESTED (out of capability class, per fairness rule 3) ----");
  console.log("auto-supersede-from-text: with NO edge, does admit() mark the stale A cell superseded");
  console.log("or drop it from active() upon seeing B's text? This build has no raw-text contradiction");
  console.log("detector at admission (admission.js acts only on declared edges), so this is UNTESTED,");
  console.log("not scored as a failure. The undeclared arm above scores only the ranking surface.");
  console.log("");

  const t1 = Date.now === undefined ? 0 : Date.now();
  console.log(`elapsed: ${((t1 - t0) / 1000).toFixed(1)}s`);
}

function pct(x) {
  return `${Math.round(x * 100)}%`;
}

main();
