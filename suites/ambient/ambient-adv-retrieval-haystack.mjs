#!/usr/bin/env node
// AMBIENT ADVERSARIAL - retrieval-haystack.
//
// Axis: as the store fills with distractor cells, does a planted needle still
// retrieve? We plant M unique needle facts, then grow the store with N
// distractors (N = 10, 100, 1000, 5000, 20000) and, at each N, run every
// needle's query against the three model-free retrieval paths the build claims
// via MCP:
//   1. lexical      -> store.search(query, {limit})           (recall_search)
//   2. semantic     -> semanticSearch(query, store, {limit})  (recall_semantic)
//   3. packet       -> compileContext(...).expansionHandles   (recall_compile)
// Metrics per (regime, backend, N): precision@1 and recall@k (k=5,10), averaged
// over the M needles.
//
// FAIRNESS (both-direction headroom by construction):
//   * UNRELATED regime: distractors are filler drawn from a vocab DISJOINT from
//     every needle token. The query terms are unique to the needle, so only the
//     needle can match. This regime CAN ONLY PASS (needle survives). It exists to
//     prove the harness is not rigged to fail and to expose the store's true
//     ceiling under pure scale.
//   * COLLISION regime: distractors are keyword-dense cells drawn from a SHARED
//     vocab that the needle queries also draw from, generated by a deterministic
//     rule (hash of the index, no RNG). A scheduled fraction are terse near-
//     duplicate decoys carrying exactly the query terms and nothing else, so
//     BM25 length-normalization ranks them ABOVE the (slightly longer, serial-
//     bearing) real needle. This regime CAN FAIL, and its break is a curve in N,
//     not a single point. The ONLY difference between the two regimes is the
//     distractor-generation rule; the needles and queries are identical.
//   * ABSENT queries (false-positive / precision headroom): queries whose answer
//     is genuinely not in the store. NOVEL-absent uses tokens that appear
//     nowhere; OVERLAP-absent uses a SHARED-token combination that no single
//     cell holds. We record whether each backend returns a spurious top-1 and
//     whether a score threshold separates "present" from "absent".
//
// Same-capability-class: we score only the three retrieval primitives the store
// exposes. Point-in-time / as-of retrieval and auto-detection of the needle's
// meaning from raw text are NOT claimed here and are left UNTESTED.
//
// Deterministic: no RNG, no wall-clock in any decision. Date.now() is used only
// to measure build/query latency for reporting. now= is pinned for compile.

import {
  SqliteStore,
  admit,
  buildCell,
  indexCell,
  semanticSearch,
  compileContext,
} from "./_recall.mjs";

// ---------------------------------------------------------------------------
// Deterministic hash (FNV-1a, no RNG).
// ---------------------------------------------------------------------------
function h32(x) {
  let v = 2166136261 >>> 0;
  const s = String(x);
  for (let i = 0; i < s.length; i++) {
    v ^= s.charCodeAt(i);
    v = Math.imul(v, 16777619) >>> 0;
  }
  return v >>> 0;
}

// ---------------------------------------------------------------------------
// Vocabularies. SHARED and FILLER are disjoint, and neither contains any needle
// token from the UNRELATED regime (which uses a rare "qzx" prefix).
// ---------------------------------------------------------------------------
const SHARED = [
  "turbine", "calibration", "constant", "hertz", "rotor", "bearing",
  "voltage", "frequency", "pressure", "torque", "spectrum", "harmonic",
];
const FILLER = [
  "lorem", "ipsum", "dolor", "amet", "consectetur", "adipiscing", "elit",
  "tempor", "incididunt", "labore", "magna", "aliqua", "veniam", "nostrud",
  "laboris", "aliquip", "commodo", "consequat", "reprehenderit", "voluptate",
  "cillum", "fugiat", "pariatur", "excepteur", "occaecat", "cupidatat",
  "proident", "culpa", "officia", "deserunt", "mollit", "anim", "laborum",
  "fermentum", "gravida", "lacinia", "maecenas", "nullam", "vivamus", "aptent",
];

const M = 5; // needles per regime
const NOW = "2023-01-01T00:00:00.000Z";
const COMPILE_NOW = new Date("2023-01-02T00:00:00.000Z");
const CHECKPOINTS = [10, 100, 1000, 5000, 20000];

// UNRELATED needle j: 5 tokens with a rare prefix that appears in no distractor.
function unrelatedNeedleTokens(j) {
  return [
    `qzx${j}quokka`, `qzx${j}nimbus`, `qzx${j}vellum`, `qzx${j}argon`, `qzx${j}mesa`,
  ];
}
// COLLISION needle j: a distinct 5-subset of SHARED (each token is common).
const COLLISION_SUBSETS = [
  ["turbine", "calibration", "constant", "hertz", "rotor"],
  ["bearing", "voltage", "frequency", "pressure", "torque"],
  ["spectrum", "harmonic", "turbine", "voltage", "hertz"],
  ["calibration", "rotor", "frequency", "torque", "spectrum"],
  ["constant", "bearing", "pressure", "harmonic", "voltage"],
];

// ---------------------------------------------------------------------------
// Cell construction. Needles go through the genuine admit() write path; bulk
// distractors go through buildCell + store.put + indexCell, which is exactly
// what admit() does to the FTS and semantic indexes for an edge-free cell (see
// admission.js: store.put(cell) then indexCell(cell, store)). Using it lets the
// haystack reach 20000 within budget without changing a single indexed byte.
// ---------------------------------------------------------------------------
function admitNeedle(store, key, title, body, tokens) {
  const r = admit(
    {
      kind: "obs",
      title,
      body,
      confidence: 0.8,
      project: "hay",
      tenant: "local",
      topics: tokens.slice(0, 3),
      entities: [key],
    },
    { store, key, now: NOW },
  );
  if (!r.accepted) {
    throw new Error(`needle not admitted: ${title} -> ${JSON.stringify(r.issues)}`);
  }
  return r.cell.key;
}

function putDistractor(store, key, title, body, topics) {
  const cell = buildCell(
    {
      kind: "obs",
      title,
      body,
      confidence: 0.8,
      project: "hay",
      tenant: "local",
      topics,
      entities: [key],
    },
    { key, now: NOW },
  );
  store.put(cell);
  indexCell(cell, store);
}

// Deterministic pick of `count` distinct items from `vocab` seeded by `seed`.
function pick(vocab, count, seed) {
  const out = [];
  const used = new Set();
  let s = seed >>> 0;
  while (out.length < count && used.size < vocab.length) {
    s = h32(s + ":" + out.length);
    const idx = s % vocab.length;
    if (!used.has(idx)) {
      used.add(idx);
      out.push(vocab[idx]);
    }
  }
  return out;
}

// ---------------------------------------------------------------------------
// Distractor bodies.
//   unrelated(i): filler only, never any query token -> needle always survives.
//   collision(i): SHARED tokens (partial matches) for every i, PLUS a scheduled
//     terse near-duplicate decoy of one needle every 25 indices past i>=50. The
//     decoy carries only the 5 query tokens (shorter than the serial-bearing
//     real needle), so BM25 ranks it strictly above the needle. Decoy density is
//     what turns precision@1 and recall@k into a curve in N.
// ---------------------------------------------------------------------------
function unrelatedDistractor(store, i) {
  const seed = h32("unrel:" + i);
  const words = pick(FILLER, 8, seed);
  const key = "u" + String(i).padStart(7, "0");
  putDistractor(store, key, `note ${i} ${words[0]} ${words[1]}`, words.join(" "), [words[2]]);
}

function collisionDistractor(store, i) {
  const key = "c" + String(i).padStart(7, "0");
  const isDecoy = i >= 50 && i % 25 === 0;
  if (isDecoy) {
    const j = Math.floor(i / 25) % M;
    const toks = COLLISION_SUBSETS[j];
    // Terse decoy: exactly the query tokens in title and body, no serial, no
    // filler. Shorter document -> higher BM25 than the real needle.
    putDistractor(store, key, toks.join(" "), toks.join(" "), toks.slice(0, 2));
    return;
  }
  // Partial: a hash-chosen subset of SHARED (size 2..5), some filler for length.
  const seed = h32("coll:" + i);
  const size = 2 + (seed % 4); // 2..5 shared tokens
  const toks = pick(SHARED, size, seed);
  const fill = pick(FILLER, 3, h32(seed + ":f"));
  putDistractor(store, key, `reading ${i} ${toks[0]}`, [...toks, ...fill].join(" "), [toks[0]]);
}

// ---------------------------------------------------------------------------
// Retrieval backends. Each returns an ordered array of cell keys.
// ---------------------------------------------------------------------------
function lexicalRank(store, query, k) {
  return store.search(query, { limit: k }).map((hop) => hop.cell.key);
}
function lexicalScored(store, query, k) {
  return store.search(query, { limit: k }).map((hop) => ({ key: hop.cell.key, score: hop.score }));
}
function semanticRank(store, query, k) {
  return semanticSearch(query, store, { limit: k }).map((hop) => hop.cell.key);
}
function semanticScored(store, query, k) {
  return semanticSearch(query, store, { limit: k }).map((hop) => ({ key: hop.cell.key, score: hop.score }));
}
function packetRank(store, query, k) {
  const packet = compileContext(store, query, {
    limit: k,
    budgetWords: 1_000_000,
    includeHealth: false,
    includeNextActions: false,
    includeConflicts: false,
  }, COMPILE_NOW);
  return packet.expansionHandles.slice(0, k);
}

const BACKENDS = {
  lexical: lexicalRank,
  semantic: semanticRank,
  packet: packetRank,
};

// precision@1 and recall@k over the M needles for one backend at one N.
function scoreNeedles(store, needleKeys, queries, rankFn) {
  let p1 = 0, r5 = 0, r10 = 0;
  for (let j = 0; j < needleKeys.length; j++) {
    const ranked = rankFn(store, queries[j], 10);
    if (ranked[0] === needleKeys[j]) p1 += 1;
    const top5 = ranked.slice(0, 5);
    const top10 = ranked.slice(0, 10);
    if (top5.includes(needleKeys[j])) r5 += 1;
    if (top10.includes(needleKeys[j])) r10 += 1;
  }
  const n = needleKeys.length;
  return { p1: p1 / n, r5: r5 / n, r10: r10 / n };
}

// ---------------------------------------------------------------------------
// Absent-query (false-positive) probes. NOVEL uses tokens present nowhere;
// OVERLAP uses a SHARED combination held by no single cell. We record whether a
// spurious top-1 comes back and its score, so a present-vs-absent separating
// threshold can be judged.
// ---------------------------------------------------------------------------
const NOVEL_ABSENT = "abszero vermilionx obsidianq zephyrless kryptonium";
const OVERLAP_ABSENT = "turbine bearing spectrum torque pressure"; // not any needle subset

function absentProbe(store) {
  const lexNovel = lexicalScored(store, NOVEL_ABSENT, 5);
  const lexOverlap = lexicalScored(store, OVERLAP_ABSENT, 5);
  const semNovel = semanticScored(store, NOVEL_ABSENT, 5);
  const semOverlap = semanticScored(store, OVERLAP_ABSENT, 5);
  return {
    lexNovelHits: lexNovel.length,
    lexNovelTop: lexNovel[0]?.score ?? null,
    lexOverlapHits: lexOverlap.length,
    lexOverlapTop: lexOverlap[0]?.score ?? null,
    semNovelHits: semNovel.length,
    semNovelTop: semNovel[0]?.score ?? null,
    semOverlapHits: semOverlap.length,
    semOverlapTop: semOverlap[0]?.score ?? null,
  };
}

// Present reference score: score the winning needle gets (unrelated regime,
// where the needle is genuinely top-1) so we can compare against absent scores.
function presentReference(store, queries) {
  let lexSum = 0, semSum = 0, n = 0;
  for (const q of queries) {
    const lex = lexicalScored(store, q, 1)[0];
    const sem = semanticScored(store, q, 1)[0];
    if (lex) lexSum += lex.score;
    if (sem) semSum += sem.score;
    n += 1;
  }
  return { lex: lexSum / n, sem: semSum / n };
}

// ---------------------------------------------------------------------------
// One regime: plant needles, grow to each checkpoint, score.
// ---------------------------------------------------------------------------
function runRegime(name, plantNeedles, addDistractor, queries, maxN) {
  const store = new SqliteStore(":memory:");
  const needleKeys = plantNeedles(store);
  const rows = []; // { N, per-backend {p1,r5,r10}, absent, present }
  let inserted = 0;
  const tBuild0 = Date.now();
  for (const N of CHECKPOINTS) {
    if (N > maxN) break;
    while (inserted < N) {
      addDistractor(store, inserted);
      inserted += 1;
    }
    const per = {};
    for (const [bk, fn] of Object.entries(BACKENDS)) {
      per[bk] = scoreNeedles(store, needleKeys, queries, fn);
    }
    rows.push({
      N,
      per,
      absent: absentProbe(store),
      present: presentReference(store, queries),
    });
  }
  const buildMs = Date.now() - tBuild0;
  const stats = store.stats();
  store.close();
  return { name, rows, buildMs, stats, cells: inserted + needleKeys.length };
}

// UNRELATED regime setup.
function plantUnrelated(store) {
  const keys = [];
  for (let j = 0; j < M; j++) {
    const toks = unrelatedNeedleTokens(j);
    const key = "needle-unrel-" + j;
    admitNeedle(store, key, `unrelated needle ${j}`, `${toks.join(" ")} serialu${j}unique reference`, toks);
    keys.push(key);
  }
  return keys;
}
const UNREL_QUERIES = Array.from({ length: M }, (_, j) => unrelatedNeedleTokens(j).join(" "));

// COLLISION regime setup. Needle body carries the 5 query tokens PLUS a unique
// serial, making it slightly longer than a terse decoy of the same tokens.
function plantCollision(store) {
  const keys = [];
  for (let j = 0; j < M; j++) {
    const toks = COLLISION_SUBSETS[j];
    const key = "needle-coll-" + j;
    admitNeedle(store, key, toks.join(" "), `${toks.join(" ")} serialc${j}unique`, toks);
    keys.push(key);
  }
  return keys;
}
const COLL_QUERIES = COLLISION_SUBSETS.map((s) => s.join(" "));

// ---------------------------------------------------------------------------
// Run. Time-guard the 20000 tier so the whole thing stays reasonable; if the
// budget is blown we stop at 5000 and mark 20000 as not reached.
// ---------------------------------------------------------------------------
const HARD_MAX = Number(process.env.RECALL_HAY_MAXN ?? 20000);
const tStart = Date.now();

const unrel = runRegime("unrelated", plantUnrelated, unrelatedDistractor, UNREL_QUERIES, HARD_MAX);
const elapsedAfterUnrel = Date.now() - tStart;
// Cap the second regime only under an extreme budget blow-out; the collision
// build is cheap (its break is already fully captured by N=5000), so we let it
// reach the full 20000 tier to match the spec's N list.
const collMax = elapsedAfterUnrel > 180000 ? 5000 : HARD_MAX;
const coll = runRegime("collision", plantCollision, collisionDistractor, COLL_QUERIES, collMax);

const totalMs = Date.now() - tStart;

// ---------------------------------------------------------------------------
// Report.
// ---------------------------------------------------------------------------
const pct = (x) => (x * 100).toFixed(0).padStart(3) + "%";
const f2 = (x) => (x == null ? " n/a" : x.toFixed(2));

function printRegime(res) {
  console.log(`\n==================== regime: ${res.name} (${res.cells} cells, build ${res.buildMs}ms, backend=${res.stats.lexicalBackend}) ====================`);
  for (const bk of ["lexical", "semantic", "packet"]) {
    console.log(`\n  backend: ${bk}`);
    console.log("    N        precision@1   recall@5   recall@10");
    for (const row of res.rows) {
      const s = row.per[bk];
      console.log(`    ${String(row.N).padStart(6)}     ${pct(s.p1)}         ${pct(s.r5)}      ${pct(s.r10)}`);
    }
  }
  console.log("\n  absent-query false-positive probe (spurious top-1 for a query whose answer is absent):");
  console.log("    N        lex:novel(hits,score)  lex:overlap(hits,score)  sem:novel(score)  sem:overlap(score)  present(lex/sem)");
  for (const row of res.rows) {
    const a = row.absent, p = row.present;
    console.log(
      `    ${String(row.N).padStart(6)}     ${a.lexNovelHits} / ${f2(a.lexNovelTop)}` +
      `            ${a.lexOverlapHits} / ${f2(a.lexOverlapTop)}` +
      `             ${f2(a.semNovelTop)}             ${f2(a.semOverlapTop)}` +
      `           ${f2(p.lex)}/${f2(p.sem)}`,
    );
  }
}

console.log("################################################################");
console.log("# AMBIENT ADVERSARIAL - retrieval-haystack");
console.log(`# needles/regime=${M}  checkpoints=${CHECKPOINTS.join(",")}  total ${totalMs}ms`);
console.log("################################################################");
printRegime(unrel);
printRegime(coll);

// ---- machine-readable summary block ----
function curve(res, bk, metric) {
  return res.rows.map((r) => r.per[bk][metric].toFixed(2)).join(",");
}
console.log("\n---- SUMMARY (curves across N = " + res_ns(unrel).join(",") + ") ----");
function res_ns(res) { return res.rows.map((r) => r.N); }
for (const res of [unrel, coll]) {
  for (const bk of ["lexical", "semantic", "packet"]) {
    console.log(`SUMMARY ${res.name}/${bk} p@1=[${curve(res, bk, "p1")}] r@5=[${curve(res, bk, "r5")}] r@10=[${curve(res, bk, "r10")}]`);
  }
}
// absent-FP curves
for (const res of [unrel, coll]) {
  const semNovel = res.rows.map((r) => f2(r.absent.semNovelTop)).join(",");
  const semOverlap = res.rows.map((r) => f2(r.absent.semOverlapTop)).join(",");
  const lexNovelHits = res.rows.map((r) => r.absent.lexNovelHits).join(",");
  const lexOverlapHits = res.rows.map((r) => r.absent.lexOverlapHits).join(",");
  console.log(`SUMMARY ${res.name}/absent sem:novel-cos=[${semNovel}] sem:overlap-cos=[${semOverlap}] lex:novel-hits=[${lexNovelHits}] lex:overlap-hits=[${lexOverlapHits}]`);
}
console.log(`SUMMARY reached-N unrelated=${res_ns(unrel).slice(-1)[0]} collision=${res_ns(coll).slice(-1)[0]}`);
